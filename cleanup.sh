#!/bin/bash

echo "====================================="
echo "🧹 ОЧИСТКА ВРЕМЕННЫХ И ЛИШНИХ ФАЙЛОВ"
echo "====================================="

# 1. Очистка корзины
echo "🗑️ Очищаю корзину..."
sudo rm -rf ~/.Trash/*
echo "✅ Корзина очищена!"

# 2. Очистка старых кэшей (чтобы освободить больше места)
echo "📦 Очищаю кэши системы и приложений..."
rm -rf ~/Library/Caches/* >/dev/null 2>&1
echo "✅ Кэши убраны!"

# 3. Удаление установщиков (DMG) из Загрузок
echo "💾 Ищу и удаляю DMG образы из Загрузок..."
find ~/Downloads -type f -name "*.dmg" -exec rm -f {} + >/dev/null 2>&1
echo "✅ DMG удалены!"

# 4. Удаление двойников файлов-загрузок (например 'файл (1).pdf')
echo "👯 Удаляю файлы-двойники (копии) из Загрузок..."
find ~/Downloads -type f -name "* (*).*" -exec rm -f {} + >/dev/null 2>&1
echo "✅ Двойники программно удалены!"

echo "====================================="
echo "🎉 Готово! Проверьте свободное место."
