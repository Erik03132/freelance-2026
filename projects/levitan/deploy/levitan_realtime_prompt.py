# === ДИНАМИКА (инжектится в КОНЕЦ промпта, НЕ ломает prefix cache) ===
def build_dynamic_suffix(caller_id: str = "", city_hint: str = "", nearest_dates: str = "") -> str:
    """Собирает trailing-часть промпта с данными конкретного звонка."""
    parts = ["\n\n=== ДАННЫЕ ТЕКУЩЕГО ЗВОНКА ==="]
    if caller_id:
        parts.append(f"Номер клиента: {caller_id}.")
    if city_hint:
        parts.append(f"Предполагаемый город клиента: {city_hint}.")
    if nearest_dates:
        parts.append(f"Ближайшие даты вывода бройлеров: {nearest_dates}.")
    parts.append("Если клиент спрашивает про дату — назови ближайшую подходящую из списка.")
    return "\n".join(parts)


